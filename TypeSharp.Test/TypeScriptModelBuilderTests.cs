using Ajax;
using NStandard;
using System;
using System.Collections.Generic;
using System.IO;
using System.Reflection;
using Xunit;

namespace TypeSharp.Test
{
    public class TypeScriptModelBuilderTests
    {
        private static readonly Version AssemblyVersion = Assembly.LoadFrom("TypeSharp").GetName().Version;

        private string GetExpectedCode(string name)
        {
            return $"/* Generated by TypeSharp v{AssemblyVersion} */\r\n\r\n{File.ReadAllText($"{name}.ts")}";
        }

        [Fact]
        public void SimpleTest()
        {
            var types = Assembly.GetExecutingAssembly().GetTypesWhichMarkedAs<TypeScriptModelAttribute>();
            var builder = new TypeScriptModelBuilder();
            builder.CacheType<RootClass>();
            builder.CacheType<JSend>();

            var tscode = builder.Compile();
            var expectedCode = GetExpectedCode(nameof(SimpleTest));
            Assert.Equal(expectedCode, tscode);
        }

        [Fact]
        public void GenericTest1()
        {
            var builder = new TypeScriptModelBuilder();
            builder.CacheType(typeof(GenericClass<int>));
            var tscode = builder.Compile();
            var expectedCode = GetExpectedCode("GenericTest1");
            Assert.Equal(expectedCode, tscode);
        }

        [Fact]
        public void GenericTest2()
        {
            var builder = new TypeScriptModelBuilder();
            builder.CacheType(Type.GetType("TypeSharp.Test.GenericClass`1"));
            var tscode = builder.Compile(new CompileOptions
            {
                OutputNames = true,
            });
            var expectedCode = GetExpectedCode("GenericTest2");
            Assert.Equal(expectedCode, tscode);
        }

        [Fact]
        public void LiveAccountTest()
        {
            var builder = new TypeScriptModelBuilder();
            builder.CacheType(typeof(LiveAccount.LiveUser));
            var tscode = builder.Compile();
            var expectedCode = GetExpectedCode(nameof(LiveAccountTest));
            Assert.Equal(expectedCode, tscode);
        }

        [Fact]
        public void JSendTest()
        {
            var builder = new TypeScriptModelBuilder();
            builder.CacheType(typeof(JSend));
            var tscode = builder.Compile();
            var expectedCode = GetExpectedCode("JSendTest");
            Assert.Equal(expectedCode, tscode);
        }

        [Fact]
        public void GenericJSendTest()
        {
            var builder = new TypeScriptModelBuilder();
            builder.CacheType(typeof(JSend<>));
            var tscode = builder.Compile();
            var expectedCode = GetExpectedCode("GenericJSendTest");
            Assert.Equal(expectedCode, tscode);
        }

    }
}
